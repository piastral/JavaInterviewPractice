package arraysAndCollections;

public class ArrayProgram {

	public static void main(String[] args) {
		
		String[] arr3 = {"rubel","hajar","babury"};
		for(int i=2 ;i>=0;i--) {
			System.out.println(arr3[i]+" ");
		}
		
		int[]arr = {20,50,23,34};
		for(int i =arr.length-1;i>=0;i--) {
			System.out.println(arr[i]+" ");
		}
	}

}
