package coreJava1;

public class WhileLoop {

	public static void main(String[] args) {
		
		/* we are using while loop to execute the same lines of  code for multiple times 
		 * until the condition is satisfied.
		 *  while(condition){
		 *  statements;
		 *  increment/decrement
		 *  }
		 * 
		 */
		
		int i = 1; // Initialization is done only first time 
		while(i>0) {   // until the condition met it will give me output
			System.out.println( "infinte while loop "+i);
			i++; // how much we want to increment is  by +1 /-1 Or +2 -2
		}
				
				
				
	}

}
